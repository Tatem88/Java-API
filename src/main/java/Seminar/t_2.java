package Seminar;

import java.util.*;

/*
Заполнить список названиями планет Солнечной системы в произвольном порядке с повторениями.
Вывести название каждой планеты и количество его повторений в списке.
 */
public class t_2 {
    public static void main(String[] args) {
        // Создаем массив с названиями планет Солнечной системы
        String[] planetNames = {"Меркурий", "Венера", "Земля", "Марс", "Юпитер", "Сатурн", "Уран", "Нептун", "Плутон"};
        // Создаем список планет
        List<String> planets = new ArrayList<>();
        // Создаем объект для генерации случайных чисел
        Random random = new Random();
        // Генерируем 10 планет со случайными названиями из массива planetNames и добавляем их в список planets
        for (int i = 0; i < 10; i++) {
            int index = random.nextInt(planetNames.length); // генерируем случайный индекс в диапазоне от 0 до длины массива planetNames
            String planet = planetNames[index]; // получаем название планеты по сгенерированному индексу
            planets.add(planet); // добавляем планету в список
        }
        // Проходим по каждой планете в списке и выводим ее название и количество ее повторений в списке
        for (String planet : planets) {
            int count = 0; // обнуляем счетчик повторений для каждой планеты
            for (String p : planets) {
                if (planet.equals(p)) { // если текущая планета равна планете в списке, то увеличиваем счетчик повторений
                    count++;
                }
            }
            System.out.println(planet + " : " + count); // выводим название планеты и количество ее повторений
        }
    }
}
